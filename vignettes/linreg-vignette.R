## ------------------------------------------------------------------------

linreg_example$print()

## ------------------------------------------------------------------------

linreg_example$plot()

## ------------------------------------------------------------------------

residuals <- linreg_example$resid()
residuals[1:5]

## ------------------------------------------------------------------------

predicted <- linreg_example$pred()
